            <h1>Quais são os objetivos do nutricionista na academia?</h1>
            <p>As atividades de um nutricionista, no geral, são voltadas à análise dos componentes nutritivos do cardápio consumido por uma pessoa ou oferecido em uma empresa do ramo da alimentação. Em uma academia, ele é o profissional fundamental para acompanhar as demandas físicas e nutricionais dos indivíduos, assim como a relação da sua alimentação com as atividades físicas. Em outras palavras, o profissional da Nutrição analisa a necessidade de vitaminas, minerais, hidratação, proteínas, fibras, entre outros componentes fundamentais para a saúde do corpo de uma pessoa que deseja ter mais bem-estar. Ao realizar exercícios físicos, principalmente os de alto impacto, é de extrema importância que a alimentação esteja associada a essas atividades, a fim de oferecer ao organismo tudo aquilo que é necessário e nas medidas corretas. Tanto a alimentação quanto o foco dos exercícios físicos realizados na academia se associam aos objetivos do paciente. Desse modo, é possível alcançar um desempenho e os resultados esperados de maneira saudável e sem causar prejuízos ao corpo
